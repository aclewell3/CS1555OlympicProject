CREATE TABLE TEAM(
    team_id integer not null,
    olympics_id integer,
    team_name varchar2(50),
    country_id integer,
    sport_id integer,
    coach_id integer,
    CONSTRAINT TEAM_PK PRIMARY KEY(team_id) DEFERRABLE INITIALLY IMMEDIATE,
